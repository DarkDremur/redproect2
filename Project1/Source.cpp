//4 Найти сумму, разность, произведение и частное их квадратов
void task4()
{
	int a, b, c, d, p, s;
	printf("-------------------------------------------------------------------------------\n");
	printf("Введите число a\n");
	printf("-------------------------------------------------------------------------------\n");
	scanf_s("%d", &a);
	printf("-------------------------------------------------------------------------------\n");
	printf("Введите число b\n");
	printf("-------------------------------------------------------------------------------\n");
	scanf_s("%d", &b);

	c = pow(a, 2) + pow(b, 2);
	printf("-------------------------------------------------------------------------------\n");
	printf("Сумма = %d\n", c);
	printf("-------------------------------------------------------------------------------\n");

	d = pow(a, 2) - pow(b, 2);
	printf("-------------------------------------------------------------------------------\n");
	printf("Разность = %d\n", d);
	printf("-------------------------------------------------------------------------------\n");

	p = pow(a, 2) / pow(b, 2);
	printf("-------------------------------------------------------------------------------\n");
	printf("Часное = %d\n", p);
	printf("-------------------------------------------------------------------------------\n");

	s = pow(a, 2) * pow(b, 2);
	printf("-------------------------------------------------------------------------------\n");
	printf("Произведение = %d\n", s);
	printf("-------------------------------------------------------------------------------\n");
}

